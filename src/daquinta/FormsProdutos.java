    private void btnSaveRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveRecordActionPerformed
        // TODO add your handling code here:
        try {
            curRow = rs.getRow();
            rs.absolute(curRow);

            int ID = Integer.valueOf(textIDProdutos.getText());
            String nome = textNomeProdutos.getText();
            int tipo;// = Integer.valueOf(textTipoTransportes.getText());
            
            if (jRadioBtnFrescos.isSelected()) tipo=1;
            else if (jRadioBtnFrutas.isSelected()) tipo = 2;
            else tipo=3;
            String notas = jTextAreaNotas.getText();
            
            int jan=0,fev=0,mar=0,abr=0,mai=0,jun=0,jul=0,ago=0,set=0,out=0,nov=0,dez=0;
            
            if (jCheckBox1.isSelected()) jan=1;
            if (jCheckBox2.isSelected()) fev=1;
            if (jCheckBox3.isSelected()) mar=1;
            if (jCheckBox4.isSelected()) abr=1;
            if (jCheckBox5.isSelected()) mai=1;
            if (jCheckBox6.isSelected()) jun=1;
            if (jCheckBox7.isSelected()) jul=1;
            if (jCheckBox8.isSelected()) ago=1;
            if (jCheckBox9.isSelected()) set=1;
            if (jCheckBox10.isSelected()) out=1;
            if (jCheckBox11.isSelected()) nov=1;
            if (jCheckBox12.isSelected()) dez=1;
            
            int  mesesarmazenamento=0;
            mesesarmazenamento = Integer.getInteger(mesesarmazenamentoTextBox.getText());
            
            if (criarnovo == true) { //Is new
                criarnovo = false;
                conDB.inserirProdutoNovo(nome, tipo, notas, jan, fev, mar, abr, mai, jun, jul, ago, set, out, nov, dez, mesesarmazenamento);
                rs.close();
                DoConnect();
            } else if (editar == true) {
                editar = false;
                conDB.inserirProdutoEditar(ID, nome, tipo, notas, jan, fev, mar, abr, mai, jun, jul, ago, set, out, nov, dez, mesesarmazenamento);
                rs.close();
                DoConnect();
            }




            btnFirst.setEnabled(true);
            btnPrevious.setEnabled(true);
            btnNext.setEnabled(true);
            btnLast.setEnabled(true);
            btnUpdateRecord.setEnabled(true);
            btnDeleteRecord.setEnabled(true);
            btnNewRecord.setEnabled(true);

            btnSaveRecord.setEnabled(true);

            JOptionPane.showMessageDialog(FormsProdutos.this, "Guardado");
        } catch (SQLException err) {
            System.out.println(err.getMessage());
        }
    }//GEN-LAST:event_btnSaveRecordActionPerformed

