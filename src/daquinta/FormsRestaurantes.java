    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        jCheckBox1.setSelected(false);
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);
        try {
            if (rs.next()){
                btnLast.setEnabled(true);
                btnNext.setEnabled(true);
                btnPrevious.setEnabled(true);
                btnFirst.setEnabled(true);
                int id_col = rs.getInt("idrestaurantes");
                String Name = rs.getString("nome");
                String Morada = rs.getString("morada");
                int Telefone = rs.getInt("telefone");
                int NIF = rs.getInt("nif");
                String GPS = rs.getString("gps");
                String Historico = rs.getString("notas");

                textIDRestaurante.setText(Integer.toString(id_col));
                textNomeRestaurante.setText(Name);
                textMoradaRestaurante.setText(Morada);
                textTelefoneRestaurante.setText(Integer.toString(Telefone));     
                textNIFRestaurante.setText(Integer.toString(NIF)); 
                textGPSRestaurante.setText(GPS); 
                textHistoricoRestaurante.setText(Historico); 
                if(rs.getInt("totalidadeEncomenda")==1) jCheckBox1.setSelected(true);
                if(rs.getInt("localizacao")==1) 
                {
                    jRadioButton1.setSelected(true);
                    jRadioButton2.setSelected(false);
                }
                if(rs.getInt("localizacao")==2) 
                {
                     jRadioButton1.setSelected(false);
                     jRadioButton2.setSelected(true);
                }
            }
            else {
                rs.previous();
                btnLast.setEnabled(false);
                btnNext.setEnabled(false);
                btnPrevious.setEnabled(true);
                btnFirst.setEnabled(true);
                 JOptionPane.showMessageDialog(FormsRestaurantes.this, "Não existem mais Clientes");
            }
        }
        catch(SQLException err) {
            JOptionPane.showMessageDialog(FormsRestaurantes.this, err.getMessage());
        }
    }//GEN-LAST:event_btnNextActionPerformed

