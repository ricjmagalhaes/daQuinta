    private void btnSaveRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveRecordActionPerformed
        // TODO add your handling code here:
            System.out.println("A Guardar");  
            int localizacao=0;
            int ID = Integer.valueOf(textIDRestaurante.getText());
            String Name = textNomeRestaurante.getText();
            String Morada = textMoradaRestaurante.getText();
            int Telefone = Integer.valueOf(textTelefoneRestaurante.getText());
            int NIF = Integer.valueOf(textNIFRestaurante.getText());
            String GPS = textGPSRestaurante.getText();
            String Historico = textHistoricoRestaurante.getText();
            if (jCheckBox1.isSelected()) totalidade=1;
            if(jRadioButton1.isSelected()) localizacao = 1;
            if(jRadioButton2.isSelected()) localizacao = 2;      
                    
            
            if (criarnovo==true){ //Is new
                System.out.println("Novo");
                criarnovo=false;
                conDB.inserirRestauranteNovo(Name, Morada,Telefone,NIF,GPS,Historico,totalidade, localizacao);
            }
            else if (editar==true) {
                System.out.println("Editar Restaurante: " + ID);
                editar=false;
                conDB.inserirRestauranteEditar(ID, Name, Morada,Telefone,NIF,GPS,Historico,totalidade, localizacao);
            }
               
            
       
            
            btnFirst.setEnabled( true );
            btnPrevious.setEnabled( true ) ;
            btnNext.setEnabled( true );
            btnLast.setEnabled( true );
            btnUpdateRecord.setEnabled( true );
            btnDeleteRecord.setEnabled( true );
            btnNewRecord.setEnabled( true );
            
            btnSaveRecord.setEnabled(false);
            
            JOptionPane.showMessageDialog(FormsRestaurantes.this, "Guardado");
             
     
    }//GEN-LAST:event_btnSaveRecordActionPerformed

