    private void DoConnect() {
        try {
            
            String host = "jdbc:mysql://localhost:3306/" + dbName;
            String uName = "root";
            String uPass ="admin";
            con = DriverManager.getConnection(host, uName, uPass);
            
            stmt = con.createStatement();
            String sql = "SELECT * FROM produtos";
            rs = stmt.executeQuery(sql);
            
            int Tipo;
            
            rs.next();
            rs.beforeFirst();
            
            while (rs.next()){  
                Tipo = rs.getInt("tipo");
                if (Tipo == 1) {
                   String ItemList1 = rs.getString("nome"); //get the element in column "item_code"
                   modelFrescos.addElement(ItemList1); //add each item to the model
                }
                else if (Tipo == 2) {
                    String ItemList2 = rs.getString("nome"); //get the element in column "item_code"
                    modelFrutas.addElement(ItemList2); //add each item to the model
                }
            }   
        
            listFrescos.setModel(modelFrescos);
            listFruta.setModel(modelFrutas);
            
        }
        catch (SQLException err) {
            JOptionPane.showMessageDialog(ListaProdutos.this, err.getMessage());
        }
    }

